# 1. Accessing Characters Using Indexes
message = "Hello, World!"
print(message[0])  # Output: "H"
print(message[7])  # Output: "W"

# 2. Negative Indexing
message = "Hello, World!"
print(message[-1])  # Output: "!"
print(message[-6])  # Output: "W"

# 3. Slicing Strings
message = "Hello, World!"
print(message[7:12])  # Output: "World"
print(message[0:5])  # Output: "Hello"