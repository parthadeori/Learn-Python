# Example 1: Newline
print("Hello\nWorld")

# Output:
# Hello
# World

# Example 2: Tab
print("Name:\tJohn")

# Output:
# Name:    John

# Example 3: Backslash
print("Path: C:\\Users\\John")

# Output:
# Path: C:\Users\John

# Example 4: Double Quote
print("He said, \"Hello!\"")

# Output:
# He said, "Hello!"

# Example 5: Single Quote
print('She\'s happy.')

# Output:
# She's happy.