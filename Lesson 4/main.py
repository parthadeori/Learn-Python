# Integers
x = 10
y = -5
print(x, y)

# Floating Point
pi = 3.14
radius = 2.5
print(pi, radius)

# Addition
x = 5 + 3
print(x)

# Subtraction
y = 10 - 7
print(y)

# Multiplication
z = 4 * 6
print(z)

# Division
w = 15 / 3
print(w)

# Complex
c = 2 + 3j
print(c)

# Type Conversion
x = 10
y = float(x)  # Convert x to a float
z = complex(x)  # Convert x to a complex number
print(x, y, z)