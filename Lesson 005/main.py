# Addition
x = 5
y = 3

result = x + y
print(result)  # Output: 8


# Subtraction
x = 5
y = 3

result = x - y
print(result)  # Output: 2


# Multiplication
x = 5
y = 3

result = x * y
print(result)  # Output: 15


# Division
x = 10
y = 3

result = x / y
print(result)  # Output: 3.3333333333333335


# Floor Division
x = 10
y = 3

result = x // y
print(result)  # Output: 3


# Modulo
x = 10
y = 3

result = x % y
print(result)  # Output: 1